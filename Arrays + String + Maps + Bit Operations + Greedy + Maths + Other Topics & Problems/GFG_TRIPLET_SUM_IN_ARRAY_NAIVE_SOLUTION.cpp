class Solution
{
public:
    // Function to find if there exists a triplet in the
    // array A[] which sums up to X.
    bool find3Numbers(int A[], int n, int X)
    {
        // Time Complexity: O(n^3)
        // We would be using three for loops
        for (int i = 0; i < n; i++)
        {
            for (int j = i + 1; j < n; j++)
            {
                for (int k = j + 1; k < n; k++)
                {
                    int sum = A[i] + A[j] + A[k];
                    if (sum == X)
                    {
                        return true;
                    }
                }
            }
        }
        return false;
    }
};

